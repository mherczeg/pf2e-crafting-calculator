function parse_value(value_string) {
  if (value_string == '0') {
    return 0;
  }
  let value_expression = value_string.replace(/[, ]/g, '').replace(/gp/g, '00+').replace(/sp/g, '0+').replace(/cp/g, '+');
  return Function('"use strict"; return (' + value_expression + '0)')();
}

var items = []
for (category in items_by_categories) {
  let is_alchemical = false;
  let is_snare = false;
  let is_magical = false;
  let batch_size = 1;
  switch (category) {
    case "Melee Weapon":
      break;
    case "Ranged Weapon":
      break;
    case "Ammunition":
      batch_size = 10;
      break;
    case "Armor":
      break;
    case "Adventuring Gear":
      break;
    case "Shield (base)":
      break;
    case "Shield (precious material)":
      break;
    case "Snare":
      is_snare = true;
      break;
    case "Shield (specific)":
      is_magical = true;
      break;
    case "Armor Fundamental Rune":
      is_magical = true;
      break;
    case "Armor Property Rune":
      is_magical = true;
      break;
    case "Weapon Fundamental Rune":
      is_magical = true;
      break;
    case "Weapon Property Rune":
      is_magical = true;
      break;
    case "Talisman":
      is_magical = true;
      break;
    case "Potion":
      is_magical = true;
      batch_size = 4;
      break;
    case "Staff":
      is_magical = true;
      break;
    case "Magic Wand":
      is_magical = true;
      break;
    case "Specialty Wand":
      is_magical = true;
      break;
    case "Alchemical Bombs":
      is_alchemical = true;
      batch_size = 4;
      break;
    case "Alchemical Elixirs":
      is_alchemical = true;
      batch_size = 4;
      break;
    case "Alchemical Poisons":
      is_alchemical = true;
      batch_size = 4;
      break;
    case "Alchemical Tools":
      break;
      is_alchemical = true;
      break;
  }
  for (i in items_by_categories[category]) {
    let item = items_by_categories[category][i];
    item.category = category;
    item.is_alchemical = is_alchemical;
    item.is_snare = is_snare;
    item.is_magical = is_magical;
    item.batch_size = batch_size;
    item.value_cp = parse_value(item.value);
    delete item.value;
    item.rarity = item.rarity || 0; // default to common
    items.push(item);
  }
}
